    private void Show_OrderDetail(String OrderID) throws Exception
    {
        DefaultTableModel model = (DefaultTableModel)jTable_order.getModel();
        // clear jtable content
        model.setRowCount(0);
        int noOfColumns = 8;        // number of columns in user table
        Object[] row = new Object[noOfColumns];
        //System.out.println(list.size());
        for(int i = 0; i < list.size(); i++)
        {
            Product_detail productDetail = Myfunc.getProductDetail_by_ProductDetailID(list.get(i).getproductDetailID(),true);
            //System.out.println("sof : " + productDetail.getId());
            row[0] = productDetail.getId();
            Product product = Myfunc.getProduct_by_ProductDetailID(list.get(i).getproductDetailID(),true);      // If not available generate
            row[1] = product.getName();
            row[2] = product.getSubType();
            
            row[3] = productDetail.getUnitSize() + " " + Myfunc.getUnitCategoryName(product.getUnitCategoryID());
           // System.out.println(row[2]);
            float price = list.get(i).getunitPrice();
            row[4] = price;
            float qty = list.get(i).getpurchasedQty();
            row[5] = qty;
            float discountPerUnit = list.get(i).getproductDiscountPrice();
            row[6] = discountPerUnit;
            row[7] = (price - discountPerUnit) * qty;
            model.addRow(row);
        }
        
        //Setting Other Things
        jLabel_orderID.setText(OrderID);
        jLabel_orderDate.setText(order.getOrderDate().toString());
        jLabel_customerID.setText(order.getCustomerID());
        Customer customer = Myfunc.getCustomerByID(order.getCustomerID(),true);
        jLabel_customerName.setText(customer.getFirstName() + " " + customer.getLastName());
        jLabel_discount.setText(Float.toString(order.getOverallDiscountPrice()) + "/-");
        jLabel_total.setText("Rs. " + Float.toString(order.getOrderAmount()) + "/-");
        jLabel_balance.setText("Rs. " + Float.toString(order.getBalance()) + "/-");
    }

