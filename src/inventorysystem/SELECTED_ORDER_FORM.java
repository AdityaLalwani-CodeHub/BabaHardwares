        int j = JOptionPane.showConfirmDialog(null, "PAYMENT OF Rs." + paidAmount + "/- HAS BEEN DONE! ARE YOU SURE TO CANCEL THE ORDER??", "WARNING",JOptionPane.YES_NO_OPTION );
