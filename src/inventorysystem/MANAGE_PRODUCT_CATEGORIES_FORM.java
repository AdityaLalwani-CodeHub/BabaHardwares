    private void jButton_insertNewCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_insertNewCategoryActionPerformed
        // TODO add your handling code here:
        if(checkInputs())
        {
            Connection con = null;
            PreparedStatement ps = null;
            Savepoint savepoint1 = null;
            
            try {
                con = My_CNX.getConnection();
                //System.out.println("database connected");
                con.setAutoCommit(false);
                //set a Savepoint
                savepoint1 = con.setSavepoint("Savepoint1");
                
                ps = con.prepareStatement("INSERT INTO product_category(id,Name)"
                        + "values(?,?) ");
                ps.setString(1, IDGenerator.getIDforProductCategory());
                ps.setString(2, jTextField_name.getText());
             
                ps.executeUpdate();
                ps.close();
                con.commit();
                con.close();
            } catch (Exception ex) {
                 JOptionPane.showMessageDialog(null,"ADDING PRODUCT CATEGORY FAILED : " + ex.getMessage());
                try {
                    if(con != null) {
                        con.rollback(savepoint1);
                    }
                } catch (SQLException ex1) {
                    Logger.getLogger(MANAGE_ORDERS_FORM.class.getName()).log(Level.SEVERE, null, ex1);
                }
            }finally
            {
                try { if(ps != null)  ps.close(); } catch(SQLException e){}
                try { if(con != null)  con.close(); } catch(SQLException e){}
                try {
                    list = Myfunc.getProductCategoryList();
                    Show_Product_Category_in_JTable();
                } catch (SQLException ex) {
                    Logger.getLogger(MANAGE_CUSTOMERS_FORM.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Product Category List could Not Be Refreshed : " + ex.getMessage());
                    this.dispose();
                }
            }
        }else{
            JOptionPane.showMessageDialog(null, "One Or More Field Are Empty");
        }
    }//GEN-LAST:event_jButton_insertNewCategoryActionPerformed

