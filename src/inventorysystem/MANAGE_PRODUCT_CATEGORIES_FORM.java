    private void jTable_categoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_categoryMouseClicked
        // TODO add your handling code here:
        int index = jTable_category.getSelectedRow();
        
        Category category = list.get(index);
        
        jTextField_id.setText(category.getId());
        jTextField_name.setText(category.getName());
        
        ArrayList<Product> productsInCategoryList;
        try {
            productsInCategoryList = Myfunc.getProductsInCategory_by_CategoryID(category.getId());
        } catch (SQLException ex) {
            Logger.getLogger(MANAGE_PRODUCT_CATEGORIES_FORM.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Problem loading Products in The Selected Category : " + ex.getMessage());
            return;
        }
        String[] listData =  new String[productsInCategoryList.size()];
        int i = 0;
        for(Product product : productsInCategoryList)
        {
            listData[i++] = product.getName();
        }
        jList_item.setListData(listData);
    }//GEN-LAST:event_jTable_categoryMouseClicked

