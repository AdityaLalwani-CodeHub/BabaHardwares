    private void Show_Orders_In_JTable() throws Exception
    {
        DefaultTableModel model = (DefaultTableModel)jTable_order.getModel();
        // clear jtable content
        model.setRowCount(0);
        int noOfColumns = 4;        // number of columns in user table
        Object[] row = new Object[noOfColumns];
        for(int i = 0; i < list.size(); i++)
        {
            row[0] = list.get(i).getID();
            row[1] = list.get(i).getOrderDate();
            row[2] = list.get(i).getCustomerID();
            //Customer customer = Myfunc.getCustomerByID(list.get(i).getCustomerID());
            //row[3] = customer.getFirstName() + " " + customer.getLastName();
            row[3] = list.get(i).getBalance();
            model.addRow(row);
        }
        
    }

