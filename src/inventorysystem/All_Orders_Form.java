    private void jButton_showSelectedOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_showSelectedOrderActionPerformed
        // TODO add your handling code here:
        int row = jTable_order.getSelectedRow();
        if(row >= 0)
        {
            DefaultTableModel orderModel = (DefaultTableModel)jTable_order.getModel();
            String orderID = orderModel.getValueAt(row, 0).toString();
            this.dispose();
            SELECTED_ORDER_FORM of = new SELECTED_ORDER_FORM(orderID);
            of.setVisible(true);
            of.pack();
            of.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
            of.setLocationRelativeTo(null);
            
            //dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
        }
    }//GEN-LAST:event_jButton_showSelectedOrderActionPerformed

    private void jButton_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_searchActionPerformed
        // TODO add your handling code here:
        String search = jTextField_search.getText().toLowerCase();
        DefaultTableModel model = (DefaultTableModel)jTable_order.getModel();
        //TheModel model = (TheModel)jTable_products.getModel();
        try {
            list = Myfunc.getOrderListWithString(search);
            model.setRowCount(0);
            if(list.isEmpty())
            {
                JOptionPane.showMessageDialog(null,"NO ORDERS FOUND!");
            }
            else
            {
                Show_Orders_In_JTable();
            }
        } catch (SQLException ex) {
            Logger.getLogger(MANAGE_PRODUCTS_FORM.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"SEARCH NOT SUCCESSFUL :-<");
            model.setRowCount(0);
        } catch (Exception ex) {
            Logger.getLogger(MANAGE_PRODUCTS_FORM.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"The List Could NOT BE Refreshed!");
        }
    }//GEN-LAST:event_jButton_searchActionPerformed

